#include <stdlib.h>
#include "libft.h"

t_list	*ft_lstmap(t_list *lst, t_list *(*f)(t_list *elem))
{
	t_list	*new;
	t_list	*tmp;

	if (!lst || !f)
		return (NULL);
	new = NULL;
	while (lst)
	{
		tmp = f(lst);
		if (!tmp)
			return (NULL);
		ft_lstpush(&new, tmp);
		lst = lst->next;
		free(tmp);
	}
	return (new);
}
